const CHROMATIC_FAMILIES = {
  primary: ["sky", "blue", "cyan", "indigo"],
  accent: ["violet", "purple", "fuchsia"],
  secondary: ["pink"],
  success: ["emerald", "green", "teal", "lime"],
  warning: ["amber", "orange", "yellow"],
  danger: ["red", "rose"],
};

const NEUTRAL_FAMILIES = ["slate", "zinc", "neutral", "gray", "stone"];

function oklch(hue, shade, specs) {
  const [l, c] = specs[shade];
  return `oklch(${l} ${c} ${hue})`;
}

function emitFamilyVars(hueKey, families, hues, specs) {
  const lines = [];
  const hue = hues[hueKey];
  for (const family of families) {
    for (const shade of PALETTE_SHADES) {
      lines.push(`  --color-${family}-${shade}: ${oklch(hue, shade, specs)};`);
    }
  }
  return lines;
}

function generateThemeBlock(themeId, hues) {
  const lines = [`html[data-theme="${themeId}"] {`];

  for (const family of NEUTRAL_FAMILIES) {
    for (const shade of PALETTE_SHADES) {
      lines.push(
        `  --color-${family}-${shade}: ${oklch(hues.base, shade, NEUTRAL_SPECS)};`,
      );
    }
  }

  for (const [hueKey, families] of Object.entries(CHROMATIC_FAMILIES)) {
    lines.push(...emitFamilyVars(hueKey, families, hues, CHROMATIC_SPECS));
  }

  lines.push("}");
  return lines.join("\n");
}

const header = `/* AUTO-GENERATED by scripts/generate-theme-palettes.mjs - do not edit manually */
/* Remaps Tailwind palette CSS variables so status/badge colors follow the active theme. */
`;

const body = Object.entries(THEME_PALETTE_HUES)
  .map(([id, hues]) => generateThemeBlock(id, hues))
  .join("\n\n");

const outPath = path.join(__dirname, "../src/app/theme-palettes.css");
writeFileSync(outPath, `${header}\n${body}\n`);
console.log(`Wrote ${outPath}`);
